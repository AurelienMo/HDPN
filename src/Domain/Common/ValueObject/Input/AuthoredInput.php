namespace App\Domain\Common\ValueObject\Input;

use App\Entity\User;

/**
 * Class AuthoredInput
 */
class AuthoredInput implements AuthoredInputInterface
{
    /** @var User */
    protected $owner;

    /**
     * @return User
     */
    public function getOwner(): User
    {
        return $this->owner;
    }

    /**
     * @param User $owner
     */
    public function setOwner(User $owner): void
    {
        $this->owner = $owner;
    }
}
