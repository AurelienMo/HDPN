namespace App\Domain\Article\Add;

/**
 * Class ImageInput
 */
class ImageInput
{
    /** @var string|null */
    protected $image;

    /**
     * @return string|null
     */
    public function getImage(): ?string
    {
        return $this->image;
    }

    /**
     * @param string|null $image
     */
    public function setImage(?string $image): void
    {
        $this->image = $image;
    }
}
